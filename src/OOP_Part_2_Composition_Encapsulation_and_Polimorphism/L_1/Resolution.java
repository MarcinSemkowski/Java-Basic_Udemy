package OOP_Part_2_Composition_Encapsulation_and_Polimorphism.L_1;

public class Resolution {
    private int weight;
    private int height;

    public Resolution(int weight, int height) {
        this.weight = weight;
        this.height = height;
    }

    public int getWeight() {
        return weight;
    }

    public int getHeight() {
        return height;
    }
}
